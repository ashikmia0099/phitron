#include<iostream>
using namespace std;
int main()
{
    int a;
    cin>>a;
    switch (a)
    {
    case 1:
        cout<<"one"<<endl;
        break;
    
    case 2:
        cout<<"two"<<endl;
        break;

    case 3:
        cout<<"Three"<<endl;
        break;
    
    case 4:
        cout<<"Four"<<endl;

    case 5:
        cout<<"Five"<<endl;
    
    default:
        break;
    }
}